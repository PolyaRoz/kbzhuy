async def seed_tray_if_empty(db: AsyncSession) -> None:
    """If posts table is empty, create system user (if needed) and seed posts."""
    existing = (await db.execute(select(Post.id).limit(1))).first()
    if existing is not None:
        return  # Already seeded

    # Find or create system user
    system_user = (
        await db.execute(select(User).where(User.email == SYSTEM_EMAIL))
    ).scalar_one_or_none()
    if system_user is None:
        system_user = User(
            email=SYSTEM_EMAIL,
            password_hash=hash_password("kbzhuy-system-no-login"),
            auth_provider="system",
        )
        db.add(system_user)
        await db.flush()

    for data in SEED_POSTS:
        db.add(
            Post(
                user_id=system_user.id,
                category=data["category"],
                title=data["title"],
                text=data["text"],
                image_url=data["image_url"],
            )
        )
    await db.commit()
